    public void informacao() {

        lbBalcaoAtNr.setText(lbBalcaoAtNr.getText() + " " + this.modCaixa.getBalcao());
        lbNomeCaixa.setText(this.modCaixa.getNome());

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        lbData.setText(sdf.format(new Date()));

        Timer t = new Timer();
        t.scheduleAtFixedRate(new relogio(), 0, 1000);
    }

    public class relogio extends TimerTask {

        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");

        public void run() {
            lbHoras.setText(sdf.format(new Date()));
        }
    }

