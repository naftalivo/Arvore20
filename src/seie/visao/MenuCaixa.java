    private void btnLevantamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLevantamentoActionPerformed
        String nrDaConta = JOptionPane.showInputDialog("Digite o Número da conta");
        Conta conta = null; int i = 0;
        this.contas = crudConta.contas();
        if (nrDaConta.matches("[0-9]{1,}")) {
            for (Conta c : contas) {
                if (c.getNumero() == Long.parseLong(nrDaConta)) {
                    conta = c;
                    break;
                }
                i++;
            }
        } else {
            JOptionPane.showMessageDialog(null, "O número da conta digitada não é válido!");
        }

        if (conta != null) {
            new TelaLevantamentos(this.modAgencia,conta.getCliente(),this.modCaixa,conta,this, true).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Não existe Cliente com esse número de conta");
        }
    }//GEN-LAST:event_btnLevantamentoActionPerformed

